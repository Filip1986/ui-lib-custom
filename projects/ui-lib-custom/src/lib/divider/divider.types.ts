/** Visual design variant for the Divider component. */
export type DividerVariant = 'material' | 'bootstrap' | 'minimal';

/** Orientation of the Divider component. */
export type DividerOrientation = 'horizontal' | 'vertical';

/** Line style of the Divider component. */
export type DividerType = 'solid' | 'dashed' | 'dotted';

/**
 * Content alignment along the Divider.
 *
 * For horizontal dividers: `'left'`, `'center'` (default), `'right'`.
 * For vertical dividers:   `'top'`, `'center'` (default), `'bottom'`.
 */
export type DividerAlign = 'left' | 'center' | 'right' | 'top' | 'bottom';
