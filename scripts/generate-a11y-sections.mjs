/**
 * Upgrades demo pages that have a plain id="keyboard-navigation" section
 * (containing only <app-doc-keyboard-nav>) to a full id="accessibility" section.
 *
 * For each qualifying page:
 *   HTML:
 *     • Renames id="keyboard-navigation" title="Keyboard Navigation"
 *       → id="accessibility" title="Accessibility"
 *     • Wraps the keyboard-nav component with an h3 subtitle
 *
 *   TS (sections array):
 *     • If the page already has { id: 'accessibility' } entry AND
 *       { id: 'keyboard-navigation' } entry → remove the keyboard-navigation one
 *       (the HTML rename ties them together)
 *     • Otherwise → rename { id: 'keyboard-navigation' } → { id: 'accessibility',
 *       label: 'Accessibility' }
 *
 * Pages that already have id="accessibility" in their HTML are skipped.
 *
 * Run:      node scripts/generate-a11y-sections.mjs
 * Dry-run:  node scripts/generate-a11y-sections.mjs --dry-run
 */

import { readFileSync, writeFileSync, readdirSync, existsSync, statSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const DRY_RUN = process.argv.includes('--dry-run');

const PAGES_DIR = join(__dirname, '../projects/demo/src/app/pages');

// ─── HTML transformer ─────────────────────────────────────────────────────────

function transformHtml(src) {
  // Replace the keyboard-navigation section with accessibility section.
  // Matches the exact pattern generated by prior migration scripts:
  //   <app-doc-section id="keyboard-navigation" title="Keyboard Navigation">
  //     <app-doc-keyboard-nav [rows]="keyboardRows" />
  //   </app-doc-section>
  //
  // Transforms to:
  //   <app-doc-section id="accessibility" title="Accessibility">
  //     <h3 class="demo-section__subtitle">Keyboard Interaction</h3>
  //     <app-doc-keyboard-nav [rows]="keyboardRows" />
  //   </app-doc-section>

  const re =
    /(<app-doc-section\s+id="keyboard-navigation"\s+title="Keyboard Navigation">)([\s\S]*?)(<\/app-doc-section>)/g;

  return src.replace(re, (_match, _open, body, close) => {
    // Add the h3 subtitle before the keyboard-nav component (if not already present)
    const trimmed = body.trim();
    const hasSubtitle = trimmed.includes('demo-section__subtitle');
    const indent = '    '; // 4 spaces — consistent with other sections
    const newBody = hasSubtitle
      ? body
      : `\n${indent}<h3 class="demo-section__subtitle">Keyboard Interaction</h3>\n${indent}${trimmed}\n  `;
    return `<app-doc-section id="accessibility" title="Accessibility">${newBody}${close}`;
  });
}

// ─── TS transformer ───────────────────────────────────────────────────────────

function transformTs(src) {
  const hasA11yEntry = /\{\s*id:\s*'accessibility'/.test(src);
  const hasKeyNavEntry = /\{\s*id:\s*'keyboard-navigation'/.test(src);

  if (!hasKeyNavEntry) return src; // nothing to do

  if (hasA11yEntry) {
    // Page already has both entries; just remove the keyboard-navigation one
    return src.replace(/\n?\s*\{\s*id:\s*'keyboard-navigation'[^}]*\},?/g, '');
  }

  // Rename the keyboard-navigation entry to accessibility
  return src.replace(
    /\{\s*id:\s*'keyboard-navigation',\s*label:\s*'[^']*'\s*\}/g,
    "{ id: 'accessibility', label: 'Accessibility' }",
  );
}

// ─── Main loop ────────────────────────────────────────────────────────────────

let processed = 0;
let skipped = 0;
const warnings = [];

const pageFolders = readdirSync(PAGES_DIR).filter((name) => {
  const full = join(PAGES_DIR, name);
  return statSync(full).isDirectory();
});

for (const folder of pageFolders) {
  const dir = join(PAGES_DIR, folder);
  const files = readdirSync(dir).filter(
    (f) => f.endsWith('.component.ts') && !f.includes('example') && !f.includes('spec'),
  );
  if (files.length === 0) {
    skipped++;
    continue;
  }

  const tsFile = files.find((f) => f.includes('demo') || f.includes(folder)) ?? files[0];
  const htmlFile = tsFile.replace('.ts', '.html');
  const tsPath = join(dir, tsFile);
  const htmlPath = join(dir, htmlFile);

  if (!existsSync(htmlPath)) {
    skipped++;
    continue;
  }

  const htmlSrc = readFileSync(htmlPath, 'utf8');

  // Skip if already on accessibility section
  if (htmlSrc.includes('id="accessibility"')) {
    skipped++;
    continue;
  }
  // Skip if no keyboard-navigation section to upgrade
  if (!htmlSrc.includes('id="keyboard-navigation"')) {
    skipped++;
    continue;
  }

  const tsSrc = readFileSync(tsPath, 'utf8');

  const newHtml = transformHtml(htmlSrc);
  const newTs = transformTs(tsSrc);

  const tsChanged = newTs !== tsSrc;
  const htmlChanged = newHtml !== htmlSrc;

  if (!tsChanged && !htmlChanged) {
    warnings.push(`⚠  ${folder}: nothing changed — check manually`);
    skipped++;
    continue;
  }

  console.log(`✅ ${folder}${tsChanged ? ' (ts+html)' : ' (html only)'}`);

  if (!DRY_RUN) {
    if (htmlChanged) writeFileSync(htmlPath, newHtml, 'utf8');
    if (tsChanged) writeFileSync(tsPath, newTs, 'utf8');
  }
  processed++;
}

console.log(`\n${'─'.repeat(60)}`);
console.log(`Processed: ${processed}  |  Skipped: ${skipped}`);
if (warnings.length) {
  console.log('\nWarnings:');
  warnings.forEach((w) => console.log(w));
}
if (DRY_RUN) console.log('\n[DRY RUN — no files written]');
