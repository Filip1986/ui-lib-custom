/**
 * scripts/competitive-surfaces.mjs
 *
 * Full competitive-benchmark sweep support. Extracts the public API surface of three libraries
 * from their `.d.ts` and produces the mechanizable half of every benchmark entry:
 *
 *   1. Surface dumps    — docs/_generated/{ui-lib,primeng,material}-api-surface.{json,md}
 *   2. Diff scaffolds   — docs/_generated/scaffolds/<component>.md
 *        Per ui-lib component: every PrimeNG/Material input auto-matched to a ui-lib input by
 *        name, un-matched ones flagged as candidate gaps, ui-lib-only inputs flagged as
 *        candidate 🚀. The human only fills in verdicts — the enumeration is done.
 *   3. Coverage report  — docs/_generated/coverage-report.md
 *        Whole components PrimeNG/Material ship that ui-lib has no equivalent for (roadmap), and
 *        ui-lib components with no reference equivalent.
 *
 * ui-lib is read from its local build output (run `npm run build` first); PrimeNG and Material are
 * packed at the pinned major. Facts and name-level matches only — capability verdicts stay human.
 *
 * Usage:
 *   npm run build && node scripts/competitive-surfaces.mjs            # version 19
 *   node scripts/competitive-surfaces.mjs --version 19                # explicit major
 *   node scripts/competitive-surfaces.mjs --dist dist/ui-lib-custom   # override ui-lib build dir
 */

import { existsSync, mkdirSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, join, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import {
  buildPayload,
  componentToMdLines,
  extractSurfaceFromDir,
  packPackage,
  readPackageVersion,
} from './lib/ng-surface.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const OUT_DIR = resolve(REPO_ROOT, 'docs/_generated');
const SCAFFOLD_DIR = join(OUT_DIR, 'scaffolds');

function parseArgs(argv) {
  const args = { version: '19', dist: 'dist/ui-lib-custom' };
  for (let index = 0; index < argv.length; index += 1) {
    if (argv[index] === '--version') args.version = argv[(index += 1)];
    else if (argv[index] === '--dist') args.dist = argv[(index += 1)];
  }
  return args;
}

// Anonymous / structural selectors that aren't real catalogue components.
const IGNORE_SELECTORS = new Set(['ng-component']);

// Cross-library naming variants for the same concept — first entry is the canonical key. Keeps the
// coverage report honest (e.g. ui-lib `input` ↔ PrimeNG `inputtext`, PrimeNG legacy `dropdown` ↔ `select`).
const SYNONYM_GROUPS = [
  ['input', 'inputtext'],
  ['select', 'dropdown'],
  ['toggleswitch', 'inputswitch', 'slidetoggle'],
  ['chip', 'chips'],
  ['accordion', 'expansionpanel'],
  ['listbox', 'selectionlist'],
  ['drawer', 'sidebar', 'sidenav'],
  ['datepicker', 'calendar'],
  ['progressspinner', 'spinner'],
];
const CANONICAL = new Map();
for (const group of SYNONYM_GROUPS) for (const name of group) CANONICAL.set(name, group[0]);

/** Normalize a selector to a comparison key: drop library prefix, attribute/compound parts, and alias variants. */
function normalizeName(selector) {
  const elementPart = selector.split(/[[,\s]/)[0];
  const base = elementPart
    .replace(/^(ui-lib-|mat-|cdk-|p-)/, '')
    .replace(/[^a-z0-9]/gi, '')
    .toLowerCase();
  return CANONICAL.get(base) ?? base;
}

/** Keep only real element components (drop directives and attribute-only selectors). */
function elementComponents(components) {
  const map = new Map();
  for (const [key, decl] of Object.entries(components)) {
    if (decl.kind !== 'component') continue;
    const elementSelector = decl.selectors.find(
      (sel) => /^[a-z][a-z0-9-]*$/.test(sel) && !IGNORE_SELECTORS.has(sel),
    );
    if (!elementSelector) continue;
    const norm = normalizeName(elementSelector);
    if (!norm) continue;
    // Prefer the canonically-named declaration when a synonym collision occurs (e.g. `select` over `dropdown`).
    const isCanonical =
      elementSelector
        .replace(/^(ui-lib-|mat-|cdk-|p-)/, '')
        .replace(/[^a-z0-9]/gi, '')
        .toLowerCase() === norm;
    if (!map.has(norm) || isCanonical) map.set(norm, { key, decl, selector: elementSelector });
  }
  return map;
}

function writeSurface(payload, basename, title) {
  const jsonPath = join(OUT_DIR, `${basename}.json`);
  writeFileSync(jsonPath, `${JSON.stringify(payload, null, 2)}\n`, 'utf8');
  const lines = [
    `# ${title} (generated)`,
    '',
    `> Generated by \`scripts/competitive-surfaces.mjs\` from **${payload.library}@${payload.version}** on ${payload.checkedAt}. Do not edit by hand.`,
    '',
  ];
  for (const key of Object.keys(payload.components)) {
    lines.push(...componentToMdLines(key, payload.components[key]));
  }
  writeFileSync(join(OUT_DIR, `${basename}.md`), `${lines.join('\n')}\n`, 'utf8');
}

/** Build the auto-matched input/output verdict rows for one reference library. */
function diffBindings(ours, theirs, label) {
  const ourSet = new Set(ours);
  const lines = [];
  for (const name of theirs) {
    const matched = ourSet.has(name);
    lines.push(
      `| \`${name}\` | ${matched ? `\`${name}\`` : '—'} | ${matched ? '✅ match' : '_(verdict?)_'} |`,
    );
  }
  const extras = ours.filter((name) => !theirs.includes(name));
  return { rows: lines, extras, label };
}

function writeScaffold(ourEntry, refs) {
  const { key, decl } = ourEntry;
  const lines = [
    `# Parity scaffold — \`${key}\``,
    '',
    '> Generated by `scripts/competitive-surfaces.mjs`. Auto-matches are by input/output **name**;',
    '> near-name matches (e.g. `rounded`→`pill`) and all capability verdicts are left for you.',
    '> Promote the resolved rows into the `docs/COMPETITIVE_BENCHMARKS.md` entry.',
    '',
    `ui-lib \`${key}\`: ${decl.inputs.length} inputs, ${decl.outputs.length} outputs, ` +
      `${decl.contentSlots.length} content slots, ${decl.templateQueries.length} template slots, ` +
      `${decl.methods.length} methods.`,
    '',
  ];

  for (const ref of refs) {
    lines.push(`## vs ${ref.libLabel} \`${ref.entry.key}\``, '');
    const inputDiff = diffBindings(decl.inputs, ref.entry.decl.inputs, ref.libLabel);
    lines.push(`### Inputs (${ref.libLabel}: ${ref.entry.decl.inputs.length})`, '');
    lines.push(`| ${ref.libLabel} input | ui-lib equivalent | Verdict |`, '| --- | --- | --- |');
    lines.push(...inputDiff.rows);
    lines.push('');
    if (inputDiff.extras.length) {
      lines.push(
        `**ui-lib-only inputs (candidate 🚀):** ${inputDiff.extras.map((n) => `\`${n}\``).join(', ')}`,
        '',
      );
    }
    const outputDiff = diffBindings(decl.outputs, ref.entry.decl.outputs, ref.libLabel);
    lines.push(`### Outputs (${ref.libLabel}: ${ref.entry.decl.outputs.length})`, '');
    lines.push(`| ${ref.libLabel} output | ui-lib equivalent | Verdict |`, '| --- | --- | --- |');
    lines.push(...outputDiff.rows);
    lines.push('');
    if (ref.entry.decl.templateQueries.length) {
      lines.push(
        `**${ref.libLabel} template slots:** ${ref.entry.decl.templateQueries.map((n) => `\`${n}\``).join(', ')}`,
        '',
      );
    }
  }

  writeFileSync(join(SCAFFOLD_DIR, `${key}.md`), `${lines.join('\n')}\n`, 'utf8');
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const distRoot = resolve(REPO_ROOT, args.dist);
  if (!existsSync(distRoot)) {
    throw new Error(`ui-lib build output not found at ${args.dist}. Run \`npm run build\` first.`);
  }

  // 1. Extract all three surfaces.
  const uiLib = buildPayload(
    'ui-lib-custom',
    readPackageVersion(distRoot),
    extractSurfaceFromDir(distRoot),
  );
  process.stdout.write(`ui-lib-custom: ${uiLib.componentCount} declarations\n`);

  const primengPack = packPackage(`primeng@${args.version}`);
  const materialPack = packPackage(`@angular/material@${args.version}`);
  let primeng;
  let material;
  try {
    primeng = buildPayload(
      'primeng',
      readPackageVersion(primengPack.packageRoot),
      extractSurfaceFromDir(primengPack.packageRoot),
    );
    material = buildPayload(
      'material',
      readPackageVersion(materialPack.packageRoot),
      extractSurfaceFromDir(materialPack.packageRoot),
    );
  } finally {
    rmSync(primengPack.workDir, { recursive: true, force: true });
    rmSync(materialPack.workDir, { recursive: true, force: true });
  }
  process.stdout.write(
    `primeng@${primeng.version}: ${primeng.componentCount} | material@${material.version}: ${material.componentCount}\n`,
  );

  if (!existsSync(OUT_DIR)) mkdirSync(OUT_DIR, { recursive: true });
  if (!existsSync(SCAFFOLD_DIR)) mkdirSync(SCAFFOLD_DIR, { recursive: true });
  writeSurface(uiLib, 'ui-lib-api-surface', 'ui-lib-custom API Surface');
  writeSurface(primeng, 'primeng-api-surface', 'PrimeNG API Surface');
  writeSurface(material, 'material-api-surface', 'Angular Material API Surface');

  // 2 + 3. Match element components and generate scaffolds + coverage.
  const ours = elementComponents(uiLib.components);
  const theirsP = elementComponents(primeng.components);
  const theirsM = elementComponents(material.components);

  let scaffoldCount = 0;
  for (const [norm, entry] of ours) {
    const refs = [];
    if (theirsP.has(norm)) refs.push({ libLabel: 'PrimeNG', entry: theirsP.get(norm) });
    if (theirsM.has(norm)) refs.push({ libLabel: 'Material', entry: theirsM.get(norm) });
    if (refs.length === 0) continue;
    writeScaffold(entry, refs);
    scaffoldCount += 1;
  }

  const missingFromUs = (theirs, lib) =>
    [...theirs.keys()]
      .filter((norm) => !ours.has(norm))
      .map((norm) => `${lib}: \`${theirs.get(norm).selector}\``);
  const pGaps = missingFromUs(theirsP, 'PrimeNG');
  const mGaps = missingFromUs(theirsM, 'Material');
  const oursExtra = [...ours.keys()]
    .filter((norm) => !theirsP.has(norm) && !theirsM.has(norm))
    .map((norm) => `\`${ours.get(norm).selector}\``);

  const coverage = [
    '# Component Coverage Report (generated)',
    '',
    `> Generated by \`scripts/competitive-surfaces.mjs\` on ${uiLib.checkedAt}. Element components only ` +
      '(directives and attribute selectors excluded). Matching is by normalized selector name.',
    '',
    `- ui-lib element components: **${ours.size}**`,
    `- PrimeNG: **${theirsP.size}** · Material: **${theirsM.size}**`,
    `- Diff scaffolds generated: **${scaffoldCount}**`,
    '',
    `## PrimeNG components with no ui-lib equivalent (${pGaps.length})`,
    '',
    pGaps.length ? pGaps.map((line) => `- ${line}`).join('\n') : '_None._',
    '',
    `## Material components with no ui-lib equivalent (${mGaps.length})`,
    '',
    mGaps.length ? mGaps.map((line) => `- ${line}`).join('\n') : '_None._',
    '',
    `## ui-lib components with no PrimeNG/Material equivalent (${oursExtra.length})`,
    '',
    oursExtra.length ? oursExtra.map((line) => `- ${line}`).join('\n') : '_None._',
    '',
  ];
  writeFileSync(join(OUT_DIR, 'coverage-report.md'), `${coverage.join('\n')}\n`, 'utf8');

  process.stdout.write(
    `Wrote surfaces, ${scaffoldCount} scaffolds, and coverage report to ${relative(REPO_ROOT, OUT_DIR)}\n`,
  );
}

main();
