/**
 * scripts/component-quality-metrics.mjs
 *
 * Emits mechanically-derived per-component quality signals that feed the benchmark's
 * cross-cutting dimensions — the numbers a feature table can't give you:
 *
 *   - Theming surface (Cat 5): count of distinct `--uilib-*` custom properties a component
 *     defines in its SCSS. A larger, well-named token surface is a real differentiator.
 *   - Public type quality (Cat 1): count of `any` occurrences in the component's shipped
 *     `.d.ts` (public surface only — lower is better; a rough leakage signal).
 *
 * Reads SCSS from projects/ui-lib-custom/src/lib/<name>/ and types from the build output
 * (run `npm run build` first). Output: docs/_generated/ui-lib-metrics.md.
 *
 * Usage: npm run build && node scripts/component-quality-metrics.mjs
 */

import { existsSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const LIB_DIR = resolve(REPO_ROOT, 'projects/ui-lib-custom/src/lib');
const TYPES_DIR = resolve(REPO_ROOT, 'dist/ui-lib-custom/types');
const OUT_DIR = resolve(REPO_ROOT, 'docs/_generated');

/** Recursively collect files with a given extension under a directory. */
function collectByExt(dir, ext, found = []) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) collectByExt(full, ext, found);
    else if (entry.name.endsWith(ext)) found.push(full);
  }
  return found;
}

/** Count distinct `--uilib-*` custom properties defined in a component's SCSS. */
function countThemingTokens(componentDir) {
  const tokens = new Set();
  for (const file of collectByExt(componentDir, '.scss')) {
    const text = readFileSync(file, 'utf8');
    for (const match of text.matchAll(/--uilib-[a-z0-9-]+(?=\s*:)/gi)) {
      tokens.add(match[0].toLowerCase());
    }
  }
  return tokens.size;
}

/** Count `any` occurrences in a component's shipped `.d.ts` (public-surface type leakage). */
function countPublicAny(name) {
  const dtsPath = join(TYPES_DIR, `ui-lib-custom-${name}.d.ts`);
  if (!existsSync(dtsPath)) return null;
  const matches = readFileSync(dtsPath, 'utf8').match(/\bany\b/g);
  return matches ? matches.length : 0;
}

function main() {
  if (!existsSync(TYPES_DIR)) {
    throw new Error(
      `Build output not found at ${relative(REPO_ROOT, TYPES_DIR)}. Run \`npm run build\` first.`,
    );
  }

  const names = readdirSync(LIB_DIR, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .sort();

  const rows = names.map((name) => ({
    name,
    tokens: countThemingTokens(join(LIB_DIR, name)),
    anyCount: countPublicAny(name),
  }));

  const totalTokens = rows.reduce((sum, row) => sum + row.tokens, 0);
  const withAny = rows.filter((row) => row.anyCount).length;

  const lines = [
    '# ui-lib-custom Component Quality Metrics (generated)',
    '',
    `> Generated by \`scripts/component-quality-metrics.mjs\` on ${new Date().toISOString().slice(0, 10)}. Do not edit by hand.`,
    `> Theming tokens feed Cat 5; public \`any\` count feeds Cat 1 (type quality — lower is better, \`—\` = no built \`.d.ts\`).`,
    '',
    `- Components: **${rows.length}** · total \`--uilib-*\` tokens defined: **${totalTokens}** · components with public \`any\`: **${withAny}**`,
    '',
    '| Component | `--uilib-*` tokens | Public `any` |',
    '| --------- | ------------------ | ------------ |',
    ...rows.map((row) => `| \`${row.name}\` | ${row.tokens} | ${row.anyCount ?? '—'} |`),
    '',
  ];

  writeFileSync(join(OUT_DIR, 'ui-lib-metrics.md'), `${lines.join('\n')}\n`, 'utf8');
  process.stdout.write(
    `Wrote ${relative(REPO_ROOT, join(OUT_DIR, 'ui-lib-metrics.md'))} (${rows.length} components, ${totalTokens} tokens)\n`,
  );
}

main();
