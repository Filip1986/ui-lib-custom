/**
 * scripts/extract-primeng-api.mjs
 *
 * Competitive-benchmark support tool. Packs a pinned PrimeNG version and emits its authoritative
 * public API surface (selector, inputs, outputs, content/template slots, public methods,
 * implemented interfaces, generics) — ground-truth row source for docs/COMPETITIVE_BENCHMARKS.md
 * so no competitor capability hides by simply not having a row. Facts only; verdicts stay human.
 *
 * Parsing lives in `scripts/lib/ng-surface.mjs`; this is a thin PrimeNG-specific wrapper. For the
 * full multi-library sweep (PrimeNG + Material + ui-lib + diff/coverage) use competitive-surfaces.mjs.
 *
 * Usage:
 *   node scripts/extract-primeng-api.mjs                 # version 19, JSON + Markdown
 *   node scripts/extract-primeng-api.mjs --version 19    # explicit major (npm resolves latest 19.x)
 *   node scripts/extract-primeng-api.mjs --json-only     # skip the Markdown digest
 *
 * Output:
 *   docs/_generated/primeng-api-surface.json   # machine-readable, keyed by component
 *   docs/_generated/primeng-api-surface.md     # human-eyeball digest (unless --json-only)
 */

import { existsSync, mkdirSync, rmSync, writeFileSync } from 'node:fs';
import { dirname, join, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import {
  buildPayload,
  componentToMdLines,
  extractSurfaceFromDir,
  packPackage,
  readPackageVersion,
} from './lib/ng-surface.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(__dirname, '..');
const OUT_DIR = resolve(REPO_ROOT, 'docs/_generated');

function parseArgs(argv) {
  const args = { version: '19', jsonOnly: false };
  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (token === '--version') {
      args.version = argv[index + 1];
      index += 1;
    } else if (token === '--json-only') {
      args.jsonOnly = true;
    }
  }
  return args;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  const { packageRoot, workDir } = packPackage(`primeng@${args.version}`);

  try {
    const version = readPackageVersion(packageRoot);
    process.stdout.write(`Resolved primeng@${version}. Parsing declarations …\n`);

    const components = extractSurfaceFromDir(packageRoot);
    const payload = buildPayload('primeng', version, components);
    const sortedKeys = Object.keys(payload.components);

    if (!existsSync(OUT_DIR)) mkdirSync(OUT_DIR, { recursive: true });
    const jsonPath = join(OUT_DIR, 'primeng-api-surface.json');
    writeFileSync(jsonPath, `${JSON.stringify(payload, null, 2)}\n`, 'utf8');
    process.stdout.write(
      `Wrote ${relative(REPO_ROOT, jsonPath)} (${sortedKeys.length} declarations)\n`,
    );

    if (!args.jsonOnly) {
      const lines = [
        '# PrimeNG API Surface (generated)',
        '',
        `> Generated by \`scripts/extract-primeng-api.mjs\` from **primeng@${version}** on ${payload.checkedAt}.`,
        '> Do not edit by hand. This is ground-truth row source for `docs/COMPETITIVE_BENCHMARKS.md`.',
        '',
      ];
      for (const key of sortedKeys) {
        lines.push(...componentToMdLines(key, payload.components[key]));
      }
      const mdPath = join(OUT_DIR, 'primeng-api-surface.md');
      writeFileSync(mdPath, `${lines.join('\n')}\n`, 'utf8');
      process.stdout.write(`Wrote ${relative(REPO_ROOT, mdPath)}\n`);
    }
  } finally {
    rmSync(workDir, { recursive: true, force: true });
  }
}

main();
